// Helper functions
export const getDocumentType = (id: string): DocumentTypeConfig | undefined => {
    return DOCUMENT_TYPES[id];
};

export const getDocumentsByCategory = (category: DocumentTypeConfig['category']): DocumentTypeConfig[] => {
    return Object.values(DOCUMENT_TYPES).filter(doc => doc.category === category);
};

export const getAllDocumentTypes = (): DocumentTypeConfig[] => {
    return Object.values(DOCUMENT_TYPES);
};

export const getRecommendedModel = (documentId: string): string => {
    const doc = getDocumentType(documentId);
    return doc?.recommendedModel || 'gpt-4o';
};
